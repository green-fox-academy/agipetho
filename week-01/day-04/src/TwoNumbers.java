public class TwoNumbers {
    public static void main(String[] args) {
        int number1 = 13;
        int number2 = 22;

        System.out.println(number1 + number2);
        System.out.println(number2 - number1);
        System.out.println(number2 * number1);
        System.out.println ((float) number2 / number1);
        System.out.println(number2 / number1);
        System.out.println(number2 % number1);

    }
}
